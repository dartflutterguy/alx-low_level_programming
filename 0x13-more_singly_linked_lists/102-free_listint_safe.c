#include "lists.h"

/**
 * free_listint_safe -  a function that frees a listint_t list.
 * @h: pointer to the first node in the linked list
 *
 * Return: the size of the list that was freeâ€™d
 */
size_t free_listint_safe(listint_t **h)
{
	size_t ln = 0;
	int difference;
	listint_t *temp;

	if (!h || !*h)
		return (0);

	while (*h)
	{
		difference = *h - (*h)->next;
		if (difference > 0)
		{
			temp = (*h)->next;
			free(*h);
			*h = temp;
			ln++;
		}
		else
		{
			free(*h);
			*h = NULL;
			ln++;
			break;
		}
	}

	*h = NULL;

	return (ln);
}
